/**
 * Simplified SVG path strings for country border outlines
 * Normalised to an 800×400 SVG viewBox using the projectPoint formula:
 * x = (lng + 180) * (800/360)
 * y = (90 - lat) * (400/180)
 */

// Helper to convert lat/lng array to SVG path string
function borderPath(coords: Array<[number, number]>): string {
  return (
    coords
      .map(
        (c, i) =>
          `${i === 0 ? "M" : "L"}${(((c[1] + 180) * (800 / 360)).toFixed(1))},${(((90 - c[0]) * (400 / 180)).toFixed(1))}`
      )
      .join(" ") + " Z"
  );
}

