// src/lib/powerMapBorders.js
// Simplified country outlines projected into 800×400 SVG space.
// Each entry is a closed SVG polygon path string.
// Coordinates: x = (lng+180)*(800/360), y = (90-lat)*(400/180)

function p(coords) {
  return coords.map(([lat, lng], i) => {
    const x = ((lng + 180) * (800 / 360)).toFixed(1);
    const y = ((90 - lat) * (400 / 180)).toFixed(1);
    return `${i === 0 ? 'M' : 'L'}${x} ${y}`;
  }).join(' ') + ' Z';
}

