function rotateY(x, y, z, angle) {
  const cos = Math.cos(angle);
  const sin = Math.sin(angle);
  return [x * cos + z * sin, y, -x * sin + z * cos];
}

function rotateX(x, y, z, angle) {
  const cos = Math.cos(angle);
  const sin = Math.sin(angle);
  return [x, y * cos - z * sin, y * sin + z * cos];
}

function project(x, y, z, cx, cy, fov) {
  const scale = fov / (fov + z);
  return [x * scale + cx, -y * scale + cy, z];
}

export const LAND_GEOJSON_URL =
  'https://raw.githubusercontent.com/martynafford/natural-earth-geojson/refs/heads/master/110m/physical/ne_110m_land.json';

export function InteractiveGlobe({
  className,
  size = 600,
  /** Land dots — slightly brighter emerald so hero particles can sit a shade deeper without beating the globe. */
  dotColor = 'rgba(52, 211, 153, ALPHA)',
  /** Ocean fill inside the sphere (darker than typical landing bg). */
  oceanFill = '#020403',
  autoRotateSpeed = 0.5,
  showConnections = true,
  showMarkers = true,
  /** Fires once after the first frame is drawn (land data loaded). */
  onReady,
}) {
  const canvasRef = useRef(null);
  // Rotation stored in degrees [longitude, latitude] — matches reference component
  // Initial ~90° Y: centers Western Hemisphere so North & South America face the viewer
  const rotationRef = useRef([90, 0]);
  const autoRotateRef = useRef(true);
  const dragRef = useRef({
    active: false,
    startX: 0,
    startY: 0,
    startRotation: [90, 0],
  });
  const animRef = useRef(0);
  const dotsRef = useRef([]);
  const resumeTimerRef = useRef(null);
  const [loaded, setLoaded] = useState(false);
  const readyFiredRef = useRef(false);
  const onReadyRef = useRef(onReady);
  onReadyRef.current = onReady;

  // Fetch GeoJSON land data and generate continent dots
  useEffect(() => {
    let cancelled = false;

    async function loadLand() {
      try {
        const res = await fetch(LAND_GEOJSON_URL);
        if (!res.ok) throw new Error('Failed to fetch land data');
        const landGeoJSON = await res.json();
        if (cancelled) return;

        const dots = generateLandDots(landGeoJSON, 1.6);
        dotsRef.current = dots;
        setLoaded(true);
      } catch (err) {
        console.error('Globe: failed to load land data, using embedded continent dots', err);
        dotsRef.current = FALLBACK_LAND_DOTS;
        setLoaded(true);
      }
    }

    loadLand();
    return () => {
      cancelled = true;
    };
  }, []);

  const draw = useCallback(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    if (!ctx) return;

    const dpr = window.devicePixelRatio || 1;
    const w = canvas.clientWidth;
    const h = canvas.clientHeight;
    canvas.width = w * dpr;
    canvas.height = h * dpr;
    ctx.scale(dpr, dpr);

    const cx = w / 2;
    const cy = h / 2;
    const radius = Math.min(w, h) * 0.38;
    const fov = 600;

    // Auto-rotate: increment longitude in degrees each frame
    if (autoRotateRef.current && !dragRef.current.active) {
      rotationRef.current[0] += autoRotateSpeed;
    }

    // Convert degrees to radians for rotation math
    const ry = (rotationRef.current[0] * Math.PI) / 180;
    const rx = (rotationRef.current[1] * Math.PI) / 180;

    ctx.clearRect(0, 0, w, h);

    const oceanNorm = (oceanFill || '').trim().toLowerCase();
    const blendWithPage = oceanNorm === '#0a0e13';

    if (!blendWithPage) {
      const glowGrad = ctx.createRadialGradient(cx, cy, radius * 0.8, cx, cy, radius * 1.4);
      glowGrad.addColorStop(0, 'rgba(16, 185, 129, 0.02)');
      glowGrad.addColorStop(1, 'rgba(16, 185, 129, 0)');
      ctx.fillStyle = glowGrad;
      ctx.fillRect(0, 0, w, h);
    }

    // Ocean — solid disk; on landing hero matches page bg so only dots + CSS ring read
    ctx.beginPath();
    ctx.arc(cx, cy, radius, 0, Math.PI * 2);
    ctx.fillStyle = oceanFill;
    ctx.fill();

    if (!blendWithPage) {
      ctx.beginPath();
      ctx.arc(cx, cy, radius, 0, Math.PI * 2);
      ctx.strokeStyle = 'rgba(16, 185, 129, 0.08)';
      ctx.lineWidth = 1;
      ctx.stroke();
    }

    // Draw continent dots (same visual style — emerald, depth-based alpha)
    const dots = dotsRef.current;
    if (loaded && dots.length > 0 && !readyFiredRef.current) {
      readyFiredRef.current = true;
      requestAnimationFrame(() => {
        onReadyRef.current?.();
      });
    }

    for (let i = 0; i < dots.length; i++) {
      let [x, y, z] = dots[i];
      x *= radius;
      y *= radius;
      z *= radius;

      [x, y, z] = rotateX(x, y, z, rx);
      [x, y, z] = rotateY(x, y, z, ry);

      // Visible hemisphere for this projection (camera-facing): z <= 0
      if (z > 0) continue;

      // cos(angle) between surface outward normal and view — suppress limb "float" / back bleed
      const cos = -z / radius;
      if (cos <= 0.05) continue;

      const [sx, sy] = project(x, y, z, cx, cy, fov);
      // Imprinted look: circular dots (no foreshortening), tight alpha range,
      // smaller radius so they read as surface texture not floating particles
      const rawAlpha = 0.4 + cos * 0.6;
      const depthAlpha = Math.min(0.95, Math.max(0.5, Math.min(1, rawAlpha * 1.15)));
      const dotR = Math.max(0.55, 0.7 * Math.max(0.5, cos));

      ctx.save();
      ctx.translate(sx, sy);
      ctx.beginPath();
      ctx.arc(0, 0, dotR, 0, Math.PI * 2);
      ctx.fillStyle = dotColor.replace('ALPHA', depthAlpha.toFixed(2));
      ctx.fill();
      ctx.restore();
    }

    animRef.current = requestAnimationFrame(draw);
  }, [dotColor, oceanFill, autoRotateSpeed, loaded]);

  useEffect(() => {
    if (!loaded) return;
    animRef.current = requestAnimationFrame(draw);
    return () => cancelAnimationFrame(animRef.current);
  }, [draw, loaded]);

  // Drag interaction — matches reference component sensitivity and behavior
  const onPointerDown = useCallback((e) => {
    autoRotateRef.current = false;
    if (resumeTimerRef.current) clearTimeout(resumeTimerRef.current);

    dragRef.current = {
      active: true,
      startX: e.clientX,
      startY: e.clientY,
      startRotation: [...rotationRef.current],
    };
    e.target.setPointerCapture(e.pointerId);
  }, []);

  const onPointerMove = useCallback((e) => {
    if (!dragRef.current.active) return;
    const sensitivity = 0.5;
    const dx = e.clientX - dragRef.current.startX;
    const dy = e.clientY - dragRef.current.startY;

    rotationRef.current[0] = dragRef.current.startRotation[0] + dx * sensitivity;
    rotationRef.current[1] = Math.max(
      -90,
      Math.min(90, dragRef.current.startRotation[1] - dy * sensitivity),
    );
  }, []);

  const onPointerUp = useCallback(() => {
    dragRef.current.active = false;
    // Resume auto-rotation after a short delay (matches reference component)
    resumeTimerRef.current = setTimeout(() => {
      autoRotateRef.current = true;
    }, 10);
  }, []);

  // Cleanup resume timer on unmount
  useEffect(() => {
    return () => {
      if (resumeTimerRef.current) clearTimeout(resumeTimerRef.current);
    };
  }, []);

  return (
    <canvas
      ref={canvasRef}
      className={cn('w-full h-full cursor-grab active:cursor-grabbing', className)}
      style={{ width: size, height: size }}
      onPointerDown={onPointerDown}
      onPointerMove={onPointerMove}
      onPointerUp={onPointerUp}
    />
  );
}

export default InteractiveGlobe;
